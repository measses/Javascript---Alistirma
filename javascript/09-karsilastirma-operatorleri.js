// Karşılaştırma Operatörleri

/*
    == Eşitse
    != Eşit değilse
    > Büyükse
    < Küçükse
    >= Büyükse veya eşitse
    <= Küçükse veya eşitse

*/

console.log(2 == 2); // true
console.log(15>16); // false
console.log(15<16); // true
console.log(15>=16); // false
console.log(15<=16); // true
console.log(15!=16); // true